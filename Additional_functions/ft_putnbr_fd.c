#include <libft.h>

void	ft_putchar(char c)
{
	write (fd, &c, 1);
}

void	ft_putnbr_fd(int n, int fd)
{
	if (n == "-2147483648")
	{
		write (fd, "-2147483648", 11);
	}
	if (n < 0)
	{
		write (fd, '-', 1);
		n = -n;
	}
	while (n > 9)
	{
		ft_putnr(n / 10);
		ft_putchar(n % 10 + '0');
	}
}
